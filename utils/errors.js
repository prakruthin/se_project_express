// Bad Request — invalid data or invalid ID
const BAD_REQUEST = 400;

// Unauthorized — missing or invalid authentication
const UNAUTHORIZED = 401;

// Forbidden - the client is not authorized
const FORBIDDEN = 403;

// Not Found — user/item not found, or non-existent address
const NOT_FOUND = 404;

// Conflict — request conflicts with existing resource (e.g. duplicate email)
const CONFLICT = 409;

// Internal Server Error — default fallback
const INTERNAL_SERVER_ERROR = 500;

module.exports = {
  BAD_REQUEST,
  UNAUTHORIZED,
  FORBIDDEN,
  NOT_FOUND,
  CONFLICT,
  INTERNAL_SERVER_ERROR,
};
